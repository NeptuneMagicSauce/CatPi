class W : public QWidget {
 public:
  enum struct Mode { StandardIcons, ThemeIcons };

  W(auto& app, Mode mode) : QWidget(nullptr) {
    cout << "https://doc.qt.io/qt-6/qstyle.html#standardIcon" << endl;
    auto layout = new QGridLayout();
    auto i = 0;
    auto s = app.style();
    auto count = mode == Mode::StandardIcons ? qtStandardNames.size() : qtThemeNames.size();
    // std::cout << QIcon::themeName().toStdString() << endl;
    while (i < count) {
      auto b = new QPushButton();
      b->setText(QString::number(i));
      if (mode == Mode::StandardIcons) {
        b->setText(QString::fromStdString(qtStandardNames[i].first).remove("QStyle::"));
        b->setIcon(s->standardIcon((QStyle::StandardPixmap)qtStandardNames[i].second));
      } else {
        // https://doc.qt.io/qt-6/qicon.html#ThemeIcon-enum
        auto name = qtThemeNames[i].c_str();
        b->setText(QString{name} + " (" + QString::number(QIcon::hasThemeIcon(name)) + ")");
        b->setIcon(QIcon::fromTheme(name));
      }
      layout->addWidget(b, i / 4, i % 4);
      ++i;
    }
    setLayout(layout);
  }
};

int main(int argc, char** argv) {
  QApplication app{argc, argv};
  W w{app, W::Mode::StandardIcons};
  // W w{app, W::Mode::ThemeIcons};
  w.show();
  return app.exec();
}
